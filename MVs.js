//搜索指定相关的数据,返回在MVs中的位置
function getMVsP(midx,value){
	//midx是MVs得哪一个元素，value是检验相等的值
	var mP=new Array();//mP为MVs position，是指在MVs中的位置
	for(var i=0;i<MVs.length;i++)
		if(MVs[i][midx]==value) mP[mP.length++]=i;//如果符合，则记录下来
	return mP;
}

